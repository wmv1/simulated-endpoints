class HomeController < ApplicationController
  def index
    mock = Mock.find_by_path(request.fullpath)
    render json: mock.content , status: mock.status
  rescue
    flash[:alert] = "Mock \"#{request.fullpath}\" nÃ£o encontrado"
    redirect_to mocks_path
  end
end

